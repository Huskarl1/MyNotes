
public class SayisalVeriTipleri {

	public static void main(String[] args) {
		byte by = 100;
		int in = 150;
		short sh = 2000;
		long lg = 1000000;
		System.out.println("byte değeri: "+by);
		System.out.println("integer değeri: "+in);
		System.out.println("short değeri: "+sh);
		System.out.println("long değeri: "+lg);

	}

}
