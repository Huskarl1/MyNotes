
public class OndalikVeriTipleri {

	public static void main(String[] args) {
		float ft1 = 34.5f;
		float ft2 = 25.6F;
		float ft3= 48;
		System.out.println("float sayılar: ");
		System.out.println(ft1);
		System.out.println(ft2);
		System.out.println(ft3);
		
		double sayi1 = 43;
		double sayi2 = 43.5;
		System.out.println("double sayılar: ");
		System.out.println(sayi1);
		System.out.println(sayi2);
	}

}
